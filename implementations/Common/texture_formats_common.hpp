inline agpu_texture_format defaultTypedFormatForTypeless(agpu_texture_format format, bool isDepthStencil)
{
    switch (format)
    {
    case AGPU_TEXTURE_FORMAT_R32G32B32A32_TYPELESS: return AGPU_TEXTURE_FORMAT_R32G32B32A32_FLOAT;
    case AGPU_TEXTURE_FORMAT_R32G32B32_TYPELESS: return AGPU_TEXTURE_FORMAT_R32G32B32_FLOAT;
    case AGPU_TEXTURE_FORMAT_R16G16B16A16_TYPELESS: return AGPU_TEXTURE_FORMAT_R16G16B16A16_UNORM;
    case AGPU_TEXTURE_FORMAT_R32G32_TYPELESS: return AGPU_TEXTURE_FORMAT_R32G32_FLOAT;
    case AGPU_TEXTURE_FORMAT_R32G8X24_TYPELESS: return AGPU_TEXTURE_FORMAT_R32G32_UINT;
    case AGPU_TEXTURE_FORMAT_R32_FLOAT_S8X24_TYPELESS: return AGPU_TEXTURE_FORMAT_D32_FLOAT_S8X24_UINT;
    case AGPU_TEXTURE_FORMAT_R10G10B10A2_TYPELESS: return AGPU_TEXTURE_FORMAT_R10G10B10A2_UNORM;
    case AGPU_TEXTURE_FORMAT_R8G8B8A8_TYPELESS: return AGPU_TEXTURE_FORMAT_R8G8B8A8_UNORM;
    case AGPU_TEXTURE_FORMAT_R16G16_TYPELESS: return AGPU_TEXTURE_FORMAT_R16G16_UNORM;
    case AGPU_TEXTURE_FORMAT_R32_TYPELESS: return isDepthStencil ? AGPU_TEXTURE_FORMAT_D32_FLOAT  : AGPU_TEXTURE_FORMAT_R32_FLOAT;
    case AGPU_TEXTURE_FORMAT_R24G8_TYPELESS: return AGPU_TEXTURE_FORMAT_D24_UNORM_S8_UINT;
    case AGPU_TEXTURE_FORMAT_R24_UNORM_X8_TYPELESS: return AGPU_TEXTURE_FORMAT_D24_UNORM_S8_UINT;
    case AGPU_TEXTURE_FORMAT_R8G8_TYPELESS: return AGPU_TEXTURE_FORMAT_R8G8_UNORM;
    case AGPU_TEXTURE_FORMAT_R16_TYPELESS: return AGPU_TEXTURE_FORMAT_R16_UNORM;
    case AGPU_TEXTURE_FORMAT_R8_TYPELESS: return AGPU_TEXTURE_FORMAT_R8_UNORM;
    case AGPU_TEXTURE_FORMAT_BC1_TYPELESS: return AGPU_TEXTURE_FORMAT_BC1_UNORM;
    case AGPU_TEXTURE_FORMAT_BC2_TYPELESS: return AGPU_TEXTURE_FORMAT_BC2_UNORM;
    case AGPU_TEXTURE_FORMAT_BC3_TYPELESS: return AGPU_TEXTURE_FORMAT_BC3_UNORM;
    case AGPU_TEXTURE_FORMAT_BC4_TYPELESS: return AGPU_TEXTURE_FORMAT_BC4_UNORM;
    case AGPU_TEXTURE_FORMAT_BC5_TYPELESS: return AGPU_TEXTURE_FORMAT_BC5_UNORM;
    case AGPU_TEXTURE_FORMAT_B8G8R8A8_TYPELESS: return AGPU_TEXTURE_FORMAT_B8G8R8A8_UNORM;
    case AGPU_TEXTURE_FORMAT_B8G8R8X8_TYPELESS: return AGPU_TEXTURE_FORMAT_B8G8R8X8_UNORM;

    default: return format;
    }
}

